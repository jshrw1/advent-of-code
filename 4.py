def extract_extra_rolls(list):
    for i in range(0, len(list)):
        list[i] = "." + list[i]
        list[i] = list[i] + "."
    list.insert(0, "." * len(list[0]))
    list.insert(len(list), "." * len(list[0]))

    long = len(list)
    wide = len(list[0])
    access = []
    store = []

    count = 0
    loop1 = -1

    while True:
        for grid in range(1, long - 1):
            for roll in range(1, wide - 1):
                if list[grid][roll] == ".":
                    pass
                else:
                    count = 0
                    if list[grid][roll - 1] == "@":
                        count += 1
                    if list[grid][roll + 1] == "@":
                        count += 1
                    if list[grid - 1][roll - 1] == "@":
                        count += 1
                    if list[grid - 1][roll] == "@":
                        count += 1
                    if list[grid - 1][roll + 1] == "@":
                        count += 1
                    if list[grid + 1][roll - 1] == "@":
                        count += 1
                    if list[grid + 1][roll] == "@":
                        count += 1
                    if list[grid + 1][roll + 1] == "@":
                        count += 1
                    if count < 4:
                        access.append(count)
                        store.append((grid, roll))

        for item in store:
            list[item[0]] = (
                list[item[0]][: item[1]] + "." + list[item[0]][item[1] + 1 :]
            )
        store = []

        if len(access) == loop1:
            break
        loop1 = len(access)
    return print(len(access))


