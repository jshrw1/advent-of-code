    for i in range(0, len(list)):
        list[i] = "." + list[i]
        list[i] = list[i] + "."
    list.insert(0, "." * len(list[0]))
    list.insert(len(list), "." * len(list[0]))

    long = len(list)
    wide = len(list[0])
    access = 0
    for grid in range(1, long - 1):
        for roll in range(1, wide - 1):
            if list[grid][roll] == ".":
                pass
            else:
                count = 0
                if list[grid][roll - 1] == "@":
                    count += 1
                if list[grid][roll + 1] == "@":
                    count += 1
                if list[grid - 1][roll - 1] == "@":
                    count += 1
                if list[grid - 1][roll] == "@":
                    count += 1
                if list[grid - 1][roll + 1] == "@":
                    count += 1
                if list[grid + 1][roll - 1] == "@":
                    count += 1
                if list[grid + 1][roll] == "@":
                    count += 1
                if list[grid + 1][roll + 1] == "@":
                    count += 1
                if count < 4:
                    access += 1
    return print(access)


